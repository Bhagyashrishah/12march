package bhagya;

public class tableusingifelse {
	public static void main(String[] args) {
		int a=8;
		int c;
		int b=1;
		if(a==8)
		{
			if(b<=10)
			{
				System.out.print(a);
				System.out.print('*');
				System.out.print(b);
				System.out.print('=');
				c=a*b;
				System.out.println(c);
							
			}
			b++;
		}
	}

}
